const a = 7 // = representa uma atribuição
let b = 3

b += a // += atribuição aditiva, está acrescentando o valor de a em b. ( b = b + a)
console.log(b)

b -= 4 // -= atribuição subtrativa. ( b = b - 4)
console.log(b)

b *= 2 // *= atribuição multiplicativa ( b = b * 2)
console.log(b)

b /= 2 // atribuição divisiva ( b = b * 2)
console.log(b)

b %= 2 // atribuição modular ( b = b % 2), se o modulo for par o resultado será 0 e se for impar será 1.
console.log(b)